    /**
     * Evento para facturar las compras que se le realizo al proveedor
     * @param evt evento de tipo actionEvent
     */
    private void btFacturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFacturarActionPerformed
    
        Integer decision = JOptionPane.showConfirmDialog(null, "Desea guardar la factura");
        if(decision== 0)
        {
            try {
                String CARPETA = "";
                JFileChooser chooser = new JFileChooser();
                chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
                Component parent = null;
                int returnVal = chooser.showSaveDialog(parent);
                
                if (returnVal == JFileChooser.APPROVE_OPTION) {
                    String selectPath = chooser.getSelectedFile().getPath();
                    CARPETA = selectPath + File.separatorChar;
                    CARPETA += ctrlFacturaCompra.getFactura().getNroFactura() + ".pdf";
                }
                ctrlProveedor.guardarFacturaCompra(ctrlFacturaCompra.getFactura());
                ctrlProveedor.crearPDF(ctrlFacturaCompra.getFactura(), CARPETA, cbxProveedor.getSelectedIndex());
            } catch (DocumentException ex) {
                Logger.getLogger(VentanaFacturarCompras.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        else if(decision != 2)
        {
            ctrlProveedor.guardarFacturaCompra(ctrlFacturaCompra.getFactura());
        }
        
    }//GEN-LAST:event_btFacturarActionPerformed

