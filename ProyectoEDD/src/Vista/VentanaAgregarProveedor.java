    /**
     * Evento para validar el R.U.C del nuevo proveedor
     * @param evt evento de tipo KeyEvent
     */
    private void txtfRUCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfRUCKeyTyped
        if(txtfRUC.getText().length() < 12 || !ctrlProveedor.verificar_Ruc(txtfRUC.getText()))
        {
            lblRUC.setVisible(true);
        }
        else
        {
            lblRUC.setVisible(false);
            
        }
        
    }//GEN-LAST:event_txtfRUCKeyTyped

