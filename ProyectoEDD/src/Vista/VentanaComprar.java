    /**
     * Evento para cargar la informacion del producto que el cliente selecciono en la lista de productos
     * @param evt Evento de mouseclicked
     */
    private void jtListaProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListaProductosMouseClicked
        try 
        {
            generarImagen(jtListaProductos);
            Integer index = jtListaProductos.getSelectedRow();
            Integer id = ((Long) jtListaProductos.getValueAt(index, 0)).intValue();
            ctrlProducto.setProducto(ctrlProducto.getProductos().getByIndex(id - 1));
            lblProductoSelect.setText(ctrlProducto.getProducto().getNombre());
            jtCarrito.clearSelection();
        } 
        catch (Exception e) 
        {
        }
    }//GEN-LAST:event_jtListaProductosMouseClicked

    /**
     * Evento para actualizar las busquedas por cada letra que el cliente escribe en el campo de texto
     * @param evt Evento de tipo keyevent
     */
    private void txtfBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfBuscarKeyReleased

        jtListaProductos.clearSelection();

        if (txtfBuscar.getText().isBlank()) {
            cargarTabla();
        } else {
            buscar();
            jtListaProductos.setModel(modelo);
            jtListaProductos.updateUI();
        }
    }//GEN-LAST:event_txtfBuscarKeyReleased

