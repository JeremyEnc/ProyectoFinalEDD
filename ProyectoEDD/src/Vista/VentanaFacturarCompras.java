    private void cargarCBXProveedor()
    {
        cbxProveedor.removeAllItems();
        
        for (int i = 0; i < ctrlProveedor.getListaProveedores().length(); i++)
        {
            cbxProveedor.addItem(ctrlProveedor.getListaProveedores().getByIndex(i).getNombreProveedor() + " "  + ctrlProveedor.getListaProveedores().getByIndex(i).getApellidoProveedor());
        }
    }
    
    private void instanciarDatosFactura()
    {
        ctrlFacturaCompra.generarNumeroFactura();
        ctrlFacturaCompra.calcularFecha();
        
        DateFormat dfD = DateFormat.getDateInstance(DateFormat.SHORT);
        DateFormat dfH = DateFormat.getTimeInstance(DateFormat.MEDIUM);
        
        lblNroFactura.setText(ctrlFacturaCompra.getFactura().getNroFactura());
        lblFecha.setText(dfD.format(ctrlFacturaCompra.getFactura().getFecha()) + " " + dfH.format(ctrlFacturaCompra.getFactura().getFecha()));
    }
    
