    /**
     * Metodo para cargar los proveedores en el comboBox
     */
    
    private void cargarCBXProveedor()
    {
        cbxProveedor.removeAllItems();
        
        for (int i = 0; i < ctrlProveedor.getListaProveedores().length(); i++)
        {
            cbxProveedor.addItem(ctrlProveedor.getListaProveedores().getByIndex(i).getNombreProveedor() + " "  + ctrlProveedor.getListaProveedores().getByIndex(i).getApellidoProveedor());
        }
    }
    
    /**
     * Metodo para instanciar los datos de la factura
     */
    private void instanciarDatosFactura()
    {
        ctrlFacturaCompra.generarNumeroFactura();
        ctrlFacturaCompra.calcularFecha();
        
        DateFormat dfD = DateFormat.getDateInstance(DateFormat.SHORT);
        DateFormat dfH = DateFormat.getTimeInstance(DateFormat.MEDIUM);
        
        lblNroFactura.setText(ctrlFacturaCompra.getFactura().getNroFactura());
        lblFecha.setText(dfD.format(ctrlFacturaCompra.getFactura().getFecha()) + " " + dfH.format(ctrlFacturaCompra.getFactura().getFecha()));
    }
    
    /**
     * Metodo para Agregar una compra a la facturaCompra y a la tabla principal
     */
    private void agregarCompraTabla()
    {
        DefaultTableModel modelo=(DefaultTableModel) jtDetalleFactura.getModel();
        modelo.setRowCount(0);
        jtDetalleFactura.setModel(modelo);
        
        Object[] fila = new Object[4];
        
        for (int i = 0; i < ctrlFacturaCompra.getFactura().getListaDetalles().length(); i++)
        {
            fila[0] = ctrlFacturaCompra.getFactura().getListaDetalles().getByIndex(i).getIdProducto();
            fila[1] = ctrlFacturaCompra.getFactura().getListaDetalles().getByIndex(i).getCantidad();
            fila[2] = ctrlFacturaCompra.getFactura().getListaDetalles().getByIndex(i).getPrecioUnitario();
            fila[3] = ctrlFacturaCompra.getFactura().getListaDetalles().getByIndex(i).getPrecioTotal();
            modelo.addRow(fila);
        }
        
        jtDetalleFactura.setModel(modelo);
        calcularCostos();
    }
    
    /**
     * Metodo para calcular los costos de las compras al proveedor
     */
    private void calcularCostos()
    {
        DecimalFormat dc = new DecimalFormat("##.##");
        
        ctrlFacturaCompra.setIva(0.12);
        lblSubtotal.setText(Double.toString(ctrlFacturaCompra.getFactura().getSubTotal()));
        lblIVA.setText(dc.format(ctrlFacturaCompra.getFactura().getIva()));
        lblTotal.setText(ctrlFacturaCompra.getFactura().getTotal().toString());
    }

