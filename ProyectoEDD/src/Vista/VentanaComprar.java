    private void generarImagen(JTable jt) {
        Integer index = jt.getSelectedRow();
        Integer id = ((Long) jt.getValueAt(index, 0)).intValue();
        ImageIcon icon = ctrlProducto.getProductos().getByIndex(id - 1).getIcon();
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_DEFAULT));
        lblFoto.setText(null);
        lblFoto.setIcon(icono);
    }

    private void cargarTabla() {
        modelo.setLista(ctrlProducto.getProductos());
        jtListaProductos.setModel(modelo);
        jtListaProductos.updateUI();
    }

    private void ocultarColumnaID(JTable jt) {
        jt.getColumnModel().getColumn(0).setMaxWidth(0);
        jt.getColumnModel().getColumn(0).setMinWidth(0);
        jt.getColumnModel().getColumn(0).setPreferredWidth(0);
        jt.getColumnModel().getColumn(0).setResizable(false);
    }

    private void registrarDetalle() {
        Integer cantidad = Integer.valueOf(txtfCantidad.getText().trim());
        ctrlFactura.cargarDetalleFactura(ctrlProducto.getProducto().getId(), ctrlProducto.getProducto(), cantidad, ctrlProducto.getProducto().getPrecioVenta(), (ctrlProducto.getProducto().getPrecioVenta()) * cantidad);
        ctrlFactura.guardarDetalleFactura();
    }

    private void cerrarVentanasSecundarias() {
        this.setEnabled(true);
        this.toFront();
    }

    private void comprobarRol() {
        System.out.println("ComprobarRol: " + ctrlPersona.getPersona().getId_Rol().getNombreRol());
        if (ctrlPersona.getPersona().getId_Rol().getNombreRol().equals("cliente")) {
            jmFacturarCompra.setEnabled(false);
            jmFacturarCompra.setVisible(false);
            jmKardex.setEnabled(false);
            jmKardex.setVisible(false);
            jmRegistros.setEnabled(false);
            jmRegistros.setVisible(false);
            jmAgregarProducto.setEnabled(false);
            jmAgregarProducto.setVisible(false);
            jmSistema.setEnabled(false);
            jmSistema.setVisible(false);
        }

    }

