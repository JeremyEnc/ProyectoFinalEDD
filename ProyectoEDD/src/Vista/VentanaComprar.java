    private void ocultarColumnaID(JTable jt) {
        jt.getColumnModel().getColumn(0).setMaxWidth(0);
        jt.getColumnModel().getColumn(0).setMinWidth(0);
        jt.getColumnModel().getColumn(0).setPreferredWidth(0);
        jt.getColumnModel().getColumn(0).setResizable(false);
    }

    /**
     * Metodo para registrar el detalleFactura dentro del controladorFactura
     */
    private void registrarDetalle() {
        Integer cantidad = Integer.valueOf(txtfCantidad.getText().trim());
        ctrlFactura.cargarDetalleFactura(ctrlProducto.getProducto().getId(), ctrlProducto.getProducto(), cantidad, ctrlProducto.getProducto().getPrecioVenta(), (ctrlProducto.getProducto().getPrecioVenta()) * cantidad);
        ctrlFactura.guardarDetalleFactura();
    }

