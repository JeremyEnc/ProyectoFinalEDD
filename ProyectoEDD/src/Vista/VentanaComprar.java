    /**
     * Metodo para cargar la imagen del producto seleccionado
     * @param jt la tabla en la que se selecciono el producto a presentar
     */
    private void generarImagen(JTable jt) {
        Integer index = jt.getSelectedRow();
        Integer id = ((Long) jt.getValueAt(index, 0)).intValue();
        ImageIcon icon = ctrlProducto.getProductos().getByIndex(id - 1).getIcon();
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_DEFAULT));
        lblFoto.setText(null);
        lblFoto.setIcon(icono);
    }

