    /**
     * Metodo para pintar el encabezado de las tablas
     */
    private void encabezadoColor()
    {
        DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
        cellRenderer.setBackground(new java.awt.Color(51, 153, 255));
        for (int i = 0; i < 9; i++) {
            if(i < 5)
            {
                TableColumn column = jtCarrito.getTableHeader().getColumnModel().getColumn(i);
                column.setHeaderRenderer(cellRenderer);
                cellRenderer.setHorizontalAlignment(cellRenderer.CENTER);
            }
            TableColumn column = jtListaProductos.getTableHeader().getColumnModel().getColumn(i);
            column.setHeaderRenderer(cellRenderer);
            cellRenderer.setHorizontalAlignment(cellRenderer.CENTER);
        }
    }

