    private void btAgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarCarritoActionPerformed
    
        try 
        {
            if (jtListaProductos.getSelectedRow() == -1)
            {
                System.out.println("Debe seleccionar un producto");
            } 
            else 
            {
                DefaultTableModel modelo = (DefaultTableModel) jtCarrito.getModel();

                Object[] fila = new Object[5];

                fila[0] = ctrlProducto.getProducto().getId();
                fila[1] = ctrlProducto.getProducto().getNombre();
                fila[2] = txtfCantidad.getText();
                fila[3] = ctrlProducto.getProducto().getPrecioVenta();
                fila[4] = (ctrlProducto.getProducto().getPrecioVenta() * Integer.valueOf(txtfCantidad.getText()));
                modelo.addRow(fila);
                jtCarrito.setModel(modelo);
            }
            registrarDetalle();
        }
        catch (ClassCastException e) {
        }
        
    }//GEN-LAST:event_btAgregarCarritoActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed

        Integer index = jtCarrito.getSelectedRow();

        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Debe escoger un producto del carrito", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            ctrlFactura.borrarDetalle((Long) jtCarrito.getValueAt(index, 0));
            DefaultTableModel modelotmp = (DefaultTableModel) jtCarrito.getModel();
            modelotmp.removeRow(index);
        }
        jtCarrito.clearSelection();
    }//GEN-LAST:event_btRemoverActionPerformed

