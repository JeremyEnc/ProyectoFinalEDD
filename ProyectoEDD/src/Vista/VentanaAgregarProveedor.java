    /**
     * Evento para agregar un proveedor a la lista de proveedores y al sistema
     * @param evt Evento de tipo ActionEvent
     */
    private void btAgregarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarProveedorActionPerformed
        
        if(!lblRUC.isVisible())
        {
            String nombre = txtfNombre.getText().trim();
            String RUC = txtfRUC.getText().trim();
            String apellido = txtfApellido.getText().trim();
            String direccion = txtfDireccion.getText().trim();

            ctrlProveedor.agregarProveedor(RUC, nombre, apellido, direccion);
            this.dispose();
        }
        else
        { 
            JOptionPane.showMessageDialog(null, "Numero de RUC invalido");
        }
    }//GEN-LAST:event_btAgregarProveedorActionPerformed

