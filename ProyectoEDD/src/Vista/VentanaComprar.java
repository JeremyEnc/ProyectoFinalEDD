    /**
     * Metodo para buscar productos basado en la busqueda binaria y simple de las vistas
     */
    private void buscar() {
        switch (cbxCriterioBusqueda.getSelectedIndex()) {
            case 0:
                ctrlProducto.setProducto(new Producto());
                ctrlProducto.getProducto().getMarca().setNombreMarca(txtfBuscar.getText().toLowerCase());
                modelo.setLista(ctrlProducto.getProductos().multipleSearch("marca", ctrlProducto.getProducto().getMarca()));
                break;
            case 1:
                modelo.setLista(ctrlProducto.getProductos().multipleSearch("nombre", txtfBuscar.getText().toLowerCase()));
                break;
            case 2:

                for (TipoPrenda t : TipoPrenda.values()) {
                    if (t.toString().toLowerCase().startsWith(txtfBuscar.getText().toLowerCase())) {
                        modelo.setLista(ctrlProducto.getProductos().multipleSearch("tipoPrenda", TipoPrenda.valueOf(t.toString())));
                        break;
                    }
                    modelo.setLista(new Lista());
                }
                break;
            case 3:
                for (Prenda p : Prenda.values()) {
                    if (p.toString().toLowerCase().startsWith(txtfBuscar.getText().toLowerCase())) {
                        modelo.setLista(ctrlProducto.getProductos().multipleSearch("prenda", Prenda.valueOf(p.toString())));
                        break;
                    }
                    modelo.setLista(new Lista());
                }
                break;
            case 4:
                for (Color c : Color.values()) {
                    if (c.toString().toLowerCase().startsWith(txtfBuscar.getText().toLowerCase())) {
                        modelo.setLista(ctrlProducto.getProductos().multipleSearch("color", Color.valueOf(c.toString())));
                        break;
                    }
                    modelo.setLista(new Lista());
                }
                break;
        }
    }
